

<p> Amplamente difundida e praticada nos Estados Unidos, Japão e Europa, 
	a prática do tuning (personalização visual e mecânica da viatura) é já uma realidade em Portugal,
	onde a modalidade conquista adeptos diariamente. Mas afinal o que é o tuning? Para se entender melhor
	o que significa o conceito é preciso retroceder bastante na história do automóvel. Traduzido à letra, 
	tuning significa "afinação" mas, mais do que afinar, é a arte de optimização do carrotornando-o mais 
	bonito, seguro e agradável com intuito exclusivo de o adequar aos gostos do dono, tornando-o diferente e
	único. De origem inglesa, a palavra significa "sintonia fina" sendo que consensualmente se traduziu o 
	conceito para a língua portuguesa como o acto de afinar ou optimizar algo! </p>                                           
                                        
<p> Embora e erradamente se pense que o tuning é um fenómeno moderno, 
	as suas origens remontam já ao final da 2ª Guerra Mundial. Uma vez 
	terminada, esta deixou como herança para o mundo automóvel muitos amantes  
	da mecânica extremamente instruídos, fruto da sua experiência no 
	tratamento dos veículos e aviões de guerra. Os cursos em mecânica 
	que possuíam, conjuntamente com as experiências retiradas deste período, 
	permitiriam a estes jovens aplicar nos seus carros pessoais, todos os conhecimentos 
	até então adquiridos. Uma euforia que cresceu bastante quando algumas estrelas de 
	Rock N' Roll da altura começaram a aderir àquilo que ficaria conhecido como a moda 
	do Hot Roding. A competição tornou-se tão exigente que os carros começaram a ser 
	trabalhados de forma a atingir velocidades impensáveis surgindo, já nessa altura, 
	carros muito aerodinâmicos. Os então clássicos americanos eram os alvos perfeitos 
	para se aplicarem as últimas alterações idealizadas, dando origem aos "Hot Roads" 
	(os carros tunados da altura). </p>
